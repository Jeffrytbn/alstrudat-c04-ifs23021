package del.alstrudat;

public class DataFasilitas {
  public final String kode;
  public final String jenis;
  public final String nama;
  public final int kapasitas;
  
  public DataFasilitas(String kode, String jenis, String nama, int kapasitas) {
      this.kode = kode;
      this.jenis = jenis;
      this.nama = nama;
      this.kapasitas = kapasitas;
  }
}
